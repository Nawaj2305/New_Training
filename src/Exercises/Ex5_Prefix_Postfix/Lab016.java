package Exercises.Ex5_Prefix_Postfix;

//Prefix
// pre - increment - ++operand.
// value is incremented first and then stored in the result.

public class Lab016 {
    public static void main(String[] args) {

        int a=15;
        int b=++a;
        System.out.println(b);
        System.out.println(a);
    }
}
